package demo;

/**
 * A tiny class so the merge-queue demo has something real to build and test.
 */
public class Calculator {

    public int sum(int a, int b) {
        return a + b;
    }

    public int product(int a, int b) {
        return a * b;
    }

    public int subtract(int a, int b) {
        return a - b;
    }
}
